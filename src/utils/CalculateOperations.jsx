export const CalculateOperations = {
  '/': (leftOperand, rightOperand) => leftOperand / rightOperand,
  '*': (leftOperand, rightOperand) => leftOperand * rightOperand,
  '+': (leftOperand, rightOperand) => leftOperand + rightOperand,
  '-': (leftOperand, rightOperand) => leftOperand - rightOperand,
}
